defmodule MasarykEx.Commands.RestaurantMenus.CP1250 do
  # Bytes 0x80–0xFF where CP1250 codepoint != byte value.
  # ISO-8859-2 maps 0x80–0x9F as C1 controls, so those Czech chars (š=0x9A,
  # ž=0x9E, ť=0x9D …) came out as □ before this was corrected to CP1250.
  @table %{
    0x80 => 0x20AC, 0x82 => 0x201A, 0x83 => 0x0192, 0x84 => 0x201E,
    0x85 => 0x2026, 0x86 => 0x2020, 0x87 => 0x2021, 0x89 => 0x2030,
    0x8A => 0x0160, 0x8B => 0x2039, 0x8C => 0x015A, 0x8D => 0x0164,
    0x8E => 0x017D, 0x8F => 0x0179, 0x91 => 0x2018, 0x92 => 0x2019,
    0x93 => 0x201C, 0x94 => 0x201D, 0x95 => 0x2022, 0x96 => 0x2013,
    0x97 => 0x2014, 0x99 => 0x2122, 0x9A => 0x0161, 0x9B => 0x203A,
    0x9C => 0x015B, 0x9D => 0x0165, 0x9E => 0x017E, 0x9F => 0x017A,
    0xA0 => 0x00A0, 0xA1 => 0x02C7, 0xA2 => 0x02D8, 0xA3 => 0x0141,
    0xA4 => 0x00A4, 0xA5 => 0x0104, 0xAA => 0x015E, 0xAF => 0x017B,
    0xB1 => 0x0105, 0xB2 => 0x02DB, 0xB3 => 0x0142, 0xBA => 0x015F,
    0xBC => 0x013D, 0xBD => 0x02DD, 0xBE => 0x013E, 0xBF => 0x017C,
    0xC0 => 0x0154, 0xC3 => 0x0102, 0xC5 => 0x0139, 0xC6 => 0x0106,
    0xC8 => 0x010C, 0xCA => 0x0118, 0xCC => 0x011A, 0xCF => 0x010E,
    0xD0 => 0x0110, 0xD1 => 0x0143, 0xD2 => 0x0147, 0xD5 => 0x0150,
    0xD8 => 0x0158, 0xD9 => 0x016E, 0xDB => 0x0170, 0xDE => 0x0162,
    0xE0 => 0x0155, 0xE3 => 0x0103, 0xE5 => 0x013A, 0xE6 => 0x0107,
    0xE8 => 0x010D, 0xEA => 0x0119, 0xEC => 0x011B, 0xEF => 0x010F,
    0xF0 => 0x0111, 0xF1 => 0x0144, 0xF2 => 0x0148, 0xF5 => 0x0151,
    0xF8 => 0x0159, 0xF9 => 0x016F, 0xFB => 0x0171, 0xFE => 0x0163,
    0xFF => 0x02D9
  }

  @spec decode(binary()) :: String.t()
  def decode(binary) do
    binary
    |> :binary.bin_to_list()
    |> Enum.map(fn byte when byte < 0x80 -> byte; byte -> Map.get(@table, byte, byte) end)
    |> List.to_string()
  end
end
